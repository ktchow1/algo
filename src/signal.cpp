#include<iostream>
#include<csignal>
#include<thread>
#include<functional>


void sigterm_handler(int signal)
{ 
    static std::uint32_t count = 0;
    std::cout << "\n[sigterm " << signal << " handled in " << std::this_thread::get_id() << ", count = " << count << "]  " << std::flush;
    ++count;
}

